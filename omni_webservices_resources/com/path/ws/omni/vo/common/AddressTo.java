package com.path.ws.omni.vo.common;

public class AddressTo {

	private String city_to;
	private String street_to;
	private String building_to;
	private int floor_to;
	private String second_mobile_number_to;

	public String getCity_to() {
		return city_to;
	}

	public void setCity_to(String city_to) {
		this.city_to = city_to;
	}

	public String getStreet_to() {
		return street_to;
	}

	public void setStreet_to(String street_to) {
		this.street_to = street_to;
	}

	public String getBuilding_to() {
		return building_to;
	}

	public void setBuilding_to(String building_to) {
		this.building_to = building_to;
	}

	public int getFloor_to() {
		return floor_to;
	}

	public void setFloor_to(int floor_to) {
		this.floor_to = floor_to;
	}

	public String getSecond_mobile_number_to() {
		return second_mobile_number_to;
	}

	public void setSecond_mobile_number_to(String second_mobile_number_to) {
		this.second_mobile_number_to = second_mobile_number_to;
	}

}
